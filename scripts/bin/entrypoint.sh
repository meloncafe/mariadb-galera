# 공식 MariaDB 이미지 기반, Galera 클러스터 자동화 추가

set -euo pipefail

# Load common functions
SCRIPT_DIR="$(dirname "$(readlink -f "$0")")"
# shellcheck source=/dev/null
source "${SCRIPT_DIR}/../lib/common.sh"

# =============================================================================
# Galera Configuration Defaults
# =============================================================================

export GALERA_CLUSTER_NAME="${GALERA_CLUSTER_NAME:-galera_cluster}"
export GALERA_CLUSTER_ADDRESS="${GALERA_CLUSTER_ADDRESS:-}"
export GALERA_CLUSTER_BOOTSTRAP="${GALERA_CLUSTER_BOOTSTRAP:-}"
export GALERA_NODE_NAME="${GALERA_NODE_NAME:-$(hostname)}"
export GALERA_NODE_ADDRESS="${GALERA_NODE_ADDRESS:-$(get_local_ip)}"
export GALERA_SST_METHOD="${GALERA_SST_METHOD:-mariabackup}"
export GALERA_SST_USER="${GALERA_SST_USER:-mariabackup}"
export GALERA_SST_PASSWORD="${GALERA_SST_PASSWORD:-}"
export GALERA_FORCE_BOOTSTRAP="${GALERA_FORCE_BOOTSTRAP:-no}"

# Paths
GALERA_CONF_DIR="/etc/mysql/conf.d"
GALERA_CONF_FILE="${GALERA_CONF_DIR}/galera.cnf"
DATADIR="${MARIADB_DATA_DIR:-/var/lib/mysql}"
GRASTATE_FILE="${DATADIR}/grastate.dat"

# =============================================================================
# Galera Functions
# =============================================================================

check_other_nodes_available() {
    local cluster_addr="${GALERA_CLUSTER_ADDRESS#gcomm://}"

    if [[ -z "$cluster_addr" ]]; then
        return 1
    fi

    local my_ip
    my_ip=$(get_local_ip)

    local nodes
    IFS=',' read -ra nodes <<< "$cluster_addr"

    local node
    for node in "${nodes[@]}"; do
        local host="${node%%:*}"
        local port="${node##*:}"
        [[ "$port" == "$host" ]] && port=4567

        # Skip self
        local node_ip
        node_ip=$(getent hosts "$host" 2>/dev/null | awk '{print $1}' | head -1)
        if [[ "$node_ip" == "$my_ip" ]] || [[ "$host" == "$my_ip" ]]; then
            continue
        fi

        if nc -z "$host" "$port" 2>/dev/null; then
            log_info "Found active cluster node: $host:$port"
            return 0
        fi
    done

    return 1
}

is_safe_to_bootstrap() {
    if [[ -f "$GRASTATE_FILE" ]]; then
        grep -q "safe_to_bootstrap: 1" "$GRASTATE_FILE" 2>/dev/null
    else
        return 1
    fi
}

set_safe_to_bootstrap() {
    if [[ -f "$GRASTATE_FILE" ]]; then
        sed -i 's/safe_to_bootstrap: 0/safe_to_bootstrap: 1/' "$GRASTATE_FILE"
        log_info "Set safe_to_bootstrap to 1"
    fi
}

is_db_initialized() {
    [[ -d "${DATADIR}/mysql" ]]
}

should_bootstrap() {
    # Explicit bootstrap requested
    if is_boolean_yes "$GALERA_CLUSTER_BOOTSTRAP"; then
        log_info "Bootstrap requested via GALERA_CLUSTER_BOOTSTRAP"
        return 0
    fi

    # Force bootstrap (for recovery)
    if is_boolean_yes "$GALERA_FORCE_BOOTSTRAP"; then
        log_warn "Force bootstrap enabled - use with caution!"
        set_safe_to_bootstrap
        return 0
    fi

    # First time setup - no data, no other nodes
    if ! is_db_initialized && ! check_other_nodes_available; then
        log_info "First node in cluster, will bootstrap"
        return 0
    fi

    # Has data and marked safe to bootstrap, no other nodes
    if is_safe_to_bootstrap && ! check_other_nodes_available; then
        log_info "Safe to bootstrap and no other nodes found"
        return 0
    fi

    return 1
}

get_cluster_address() {
    if should_bootstrap; then
        echo "gcomm://"
    else
        echo "gcomm://${GALERA_CLUSTER_ADDRESS#gcomm://}"
    fi
}

# =============================================================================
# Configuration Generation
# =============================================================================

generate_galera_config() {
    log_info "Generating Galera configuration..."

    local cluster_address
    cluster_address=$(get_cluster_address)

    ensure_dir "$GALERA_CONF_DIR"

    cat > "$GALERA_CONF_FILE" << EOF
# Galera Cluster Configuration
# Auto-generated by Devsaurus entrypoint

[mysqld]
# Galera Provider
wsrep_on=ON
wsrep_provider=/usr/lib/galera/libgalera_smm.so

# Cluster Configuration
wsrep_cluster_name=${GALERA_CLUSTER_NAME}
wsrep_cluster_address=${cluster_address}
wsrep_node_name=${GALERA_NODE_NAME}
wsrep_node_address=${GALERA_NODE_ADDRESS}

# SST Configuration
wsrep_sst_method=${GALERA_SST_METHOD}
EOF

    # Add SST auth if password provided
    if [[ -n "$GALERA_SST_PASSWORD" ]]; then
        echo "wsrep_sst_auth=${GALERA_SST_USER}:${GALERA_SST_PASSWORD}" >> "$GALERA_CONF_FILE"
    fi

    cat >> "$GALERA_CONF_FILE" << EOF

# Required Settings for Galera
binlog_format=ROW
default_storage_engine=InnoDB
innodb_autoinc_lock_mode=2
innodb_flush_log_at_trx_commit=2

# Galera Cache
wsrep_provider_options="gcache.size=256M"
EOF

    if [[ "$cluster_address" == "gcomm://" ]]; then
        log_info "Configured as BOOTSTRAP node (new cluster)"
    else
        log_info "Configured to JOIN cluster: $cluster_address"
    fi
}

# =============================================================================
# SST User Setup
# =============================================================================

setup_sst_user() {
    # SST user setup script for /docker-entrypoint-initdb.d/
    if [[ -n "$GALERA_SST_PASSWORD" ]]; then
        local init_script="/docker-entrypoint-initdb.d/99-galera-sst-user.sql"

        cat > "$init_script" << EOF
-- Galera SST User Setup
CREATE USER IF NOT EXISTS '${GALERA_SST_USER}'@'localhost' IDENTIFIED BY '${GALERA_SST_PASSWORD}';
GRANT RELOAD, PROCESS, LOCK TABLES, REPLICATION CLIENT ON *.* TO '${GALERA_SST_USER}'@'localhost';
GRANT CREATE TABLESPACE, SUPER ON *.* TO '${GALERA_SST_USER}'@'localhost';
FLUSH PRIVILEGES;
EOF
        log_info "SST user init script created"
    fi
}

# =============================================================================
# Validation
# =============================================================================

validate_galera_config() {
    log_info "Validating Galera configuration..."

    # SST password required for cluster mode
    if [[ -n "$GALERA_CLUSTER_ADDRESS" ]] && [[ -z "$GALERA_SST_PASSWORD" ]]; then
        log_warn "GALERA_SST_PASSWORD not set - SST may fail in cluster mode"
    fi

    # Validate SST method
    case "$GALERA_SST_METHOD" in
        mariabackup|rsync|mysqldump) ;;
        *)
            log_error "Invalid GALERA_SST_METHOD: $GALERA_SST_METHOD"
            exit 1
            ;;
    esac

    log_info "Galera configuration validated"
}

# =============================================================================
# Main
# =============================================================================

main() {
    log_info "============================================"
    log_info "devsaurus/mariadb-galera"
    log_info "============================================"
    log_info "Cluster: ${GALERA_CLUSTER_NAME}"
    log_info "Node: ${GALERA_NODE_NAME} (${GALERA_NODE_ADDRESS})"

    # Validate
    validate_galera_config

    # Generate Galera config
    generate_galera_config

    # Setup SST user (will run on first init)
    if ! is_db_initialized; then
        setup_sst_user
    fi

    log_info "============================================"
    log_info "Starting MariaDB with Galera..."
    log_info "============================================"

    # Call original MariaDB entrypoint
    exec /usr/local/bin/docker-entrypoint.sh "$@"
}

main "$@"
